marca = str(input("Diz- me uma marca de carro e eu digo-te de que continente ela é originária.\nResponde com B para bmw,T para toyota, M para mustang, F para ford, N para nissan, P para porsche."))
if marca == "B":
    print("A marca BMW é europeia.")
elif marca == "T":
    print("A marca Toyota é asiática.")
elif marca == "M":
    print("A marca mustang é americana.")
elif marca == "F":
    print("A marca Ford é americana.")
elif marca == "N":
    print("A marca Nissan é asiática.")
elif marca == "P":
    print("A marca Porsche é europeia.")